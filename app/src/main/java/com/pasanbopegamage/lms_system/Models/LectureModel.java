package com.pasanbopegamage.lms_system.Models;

public class LectureModel {

    private String accountStatus,lectureEmail,lectureId,lectureImage,lectureName,timestamp,uid,userType;

    public LectureModel() {
    }

    public String getAccountStatus() {
        return accountStatus;
    }

    public void setAccountStatus(String accountStatus) {
        this.accountStatus = accountStatus;
    }

    public String getLectureEmail() {
        return lectureEmail;
    }

    public void setLectureEmail(String lectureEmail) {
        this.lectureEmail = lectureEmail;
    }

    public String getLectureId() {
        return lectureId;
    }

    public void setLectureId(String lectureId) {
        this.lectureId = lectureId;
    }

    public String getLectureImage() {
        return lectureImage;
    }

    public void setLectureImage(String lectureImage) {
        this.lectureImage = lectureImage;
    }

    public String getLectureName() {
        return lectureName;
    }

    public void setLectureName(String lectureName) {
        this.lectureName = lectureName;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getUserType() {
        return userType;
    }

    public void setUserType(String userType) {
        this.userType = userType;
    }
}
